
public class OperatorExample {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
		int b=20;
		int c=13;
		System.out.println(++b + b++);
		System.out.print(--c + c--);
	}

}
