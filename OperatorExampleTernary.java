
public class OperatorExampleTernary {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int min=(a<b)?a:b;
		System.out.println(min);
		a+=5;
		b-=4;
		System.out.println(a);
		System.out.println(b);
		a*=3;
		b/=2;
		System.out.println(a);
		System.out.println(b);
	}

}
