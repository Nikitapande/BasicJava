
public class ControlStatementExmp2 {

	public static void main(String[] args) {
		String city="Delhi";
		if(city=="Nagpur") {
			System.out.print("City is Nagpur");
		}
		else if(city =="Udaipur") {
			System.out.print("City is Udaipur");
		}
		else if(city =="Agra") {
			System.out.print("City is Agra");
		}
		else {
			System.out.print(city);
		}
	}

}
